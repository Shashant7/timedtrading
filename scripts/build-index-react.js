#!/usr/bin/env node
const fs = require("fs");
const path = require("path");
const crypto = require("crypto");
const babel = require("@babel/core");

const repoRoot = path.resolve(__dirname, "..");
const appDir = path.join(repoRoot, "react-app");
const sourcePath = path.join(appDir, "index-react.source.html");
const outputHtmlPath = path.join(appDir, "index-react.html");

function fail(message) {
  console.error(`[build-index-react] ${message}`);
  process.exit(1);
}

if (!fs.existsSync(sourcePath)) {
  fail(`Missing source file: ${sourcePath}`);
}

const sourceHtml = fs.readFileSync(sourcePath, "utf8");
const babelScriptMatch = sourceHtml.match(
  /(\s*<!-- Babel Standalone for JSX -->\s*<script[\s\S]*?<\/script>\s*)(<script type="text\/babel">)([\s\S]*?)(<\/script>)/,
);

if (!babelScriptMatch) {
  fail("Could not locate the Babel script block in index-react.source.html");
}

const jsxSource = babelScriptMatch[3];
const transpiled = babel.transformSync(jsxSource, {
  presets: [[require.resolve("@babel/preset-react"), { runtime: "classic" }]],
  comments: false,
  compact: false,
  sourceMaps: false,
  babelrc: false,
  configFile: false,
});

if (!transpiled || !transpiled.code) {
  fail("Babel transpilation returned no code");
}

const hash = crypto.createHash("sha256").update(transpiled.code).digest("hex").slice(0, 10);
const compiledFileName = `index-react.compiled.${hash}.js`;
const compiledFilePath = path.join(appDir, compiledFileName);

for (const file of fs.readdirSync(appDir)) {
  if (/^index-react\.compiled\.[a-f0-9]+\.js$/.test(file) && file !== compiledFileName) {
    fs.unlinkSync(path.join(appDir, file));
  }
}

fs.writeFileSync(
  compiledFilePath,
  `// Generated by scripts/build-index-react.js - do not edit directly\n${transpiled.code}\n`,
  "utf8",
);

let outputHtml = sourceHtml.replace(
  /\s*<!-- Babel Standalone for JSX -->\s*<script[\s\S]*?<\/script>\s*/m,
  "\n",
);

outputHtml = outputHtml.replace(
  /<script type="text\/babel">[\s\S]*?<\/script>/m,
  `<script src="./${compiledFileName}"></script>`,
);

outputHtml = outputHtml.replace(
  /App component not loaded\. Babel may not have finished processing\./g,
  "App component not loaded. Compiled app bundle may not have finished loading.",
);
outputHtml = outputHtml.replace(
  /\/\/ Poll for window\.App \(Babel may take a while on large scripts >500KB\)/,
  "// Poll for window.App in case the compiled bundle is still loading",
);
outputHtml = outputHtml.replace(
  /\[App\] Babel ready after /g,
  "[App] App bundle ready after ",
);
outputHtml = outputHtml.replace(
  /\[App\] Timed out waiting for Babel after 10s/g,
  "[App] Timed out waiting for the app bundle after 10s",
);

fs.writeFileSync(outputHtmlPath, outputHtml, "utf8");

console.log(`Built ${path.relative(repoRoot, outputHtmlPath)}`);
console.log(`Built ${path.relative(repoRoot, compiledFilePath)}`);

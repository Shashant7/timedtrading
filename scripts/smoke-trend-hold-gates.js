#!/usr/bin/env node
/**
 * Phase 3 smoke test — runs the deployed Trend-Hold gates against the
 * forensic timeline JSON produced in Phase 1.2 (data/forensic/timelines/).
 *
 * Each timeline JSON has per-inflection snapshots with the canonical
 * day-state shape (tf_tech.{D,W,4H}.ema.priceAboveEma21 + stDir,
 * monthly_bundle.supertrend_dir, td_sequential weekly counts, ...).
 * We feed those into the SAME extractTrendSnapshot + shouldPromoteToTrendHold
 * functions deployed on the live worker (worker/trend-hold.js) and
 * verify the promotions fire where the deep-dive said they should.
 *
 * Usage:
 *   node scripts/smoke-trend-hold-gates.js [--ticker SNDK] [--verbose]
 *
 * Pass criteria (matches PHASE_3_RUNBOOK.md Step 4 hard pass):
 *   - SNDK promotes at the +30% inflection (textbook RESILIENT_TREND).
 *   - No counter-example ticker (AMD/NFLX/META/PLTR/etc) promotes.
 *   - At least 80% of the 50 TH-candidate tickers have at least one
 *     promotable inflection in the timeline.
 */

import fs from 'node:fs';
import path from 'node:path';
import {
  DEFAULT_TREND_HOLD_CONFIG,
  extractTrendSnapshot,
  shouldPromoteToTrendHold,
  shouldDemoteFromTrendHold,
} from '../worker/trend-hold.js';

const args = process.argv.slice(2);
const VERBOSE = args.includes('--verbose');
const ONLY_TICKER = args.includes('--ticker') ? args[args.indexOf('--ticker') + 1] : null;

const TIMELINES_DIR = '/workspace/data/forensic/timelines';
const COHORT_JSON = '/workspace/data/cohort-segmentation.json';
const COUNTER_EXAMPLES = ['AMD', 'AEHR', 'NVDA', 'AMZN', 'TSLA', 'NFLX', 'META', 'AVGO', 'PLTR', 'PANW'];

// ─────────────────────────────────────────────────────────────────────
// Build a tickerData-like blob from a forensic-timeline inflection.
// The forensic timeline already records the same nested shape that
// the day-state KV uses, but flattens some fields. Reconstruct.
// ─────────────────────────────────────────────────────────────────────
function inflectionToTickerData(ticker, inf) {
  // The forensic snapshots have this shape:
  //   { close, daily: { ema21_above, ema5, ema12, cloud_status, stDir, rsi },
  //     weekly: { ema21_above, stDir, rsi, td9_count, td9_dir },
  //     monthly: { stDir },
  //     h4: { ema21_above, rsi },
  //     fv, fv_class, sys, ... }
  // The deployed trend-hold.js reads the day-state KV shape:
  //   tt.D.ema.priceAboveEma21, tt.D.stDir, tt.D.rsi.r5, ...
  //   monthly_bundle.supertrend_dir (PINE convention)
  // So we have to invert the normalization the timeline did + reconstruct.

  // Convert standard-convention stDir back to Pine for tf_tech inputs.
  const toPine = (std) => std === 1 ? -1 : std === -1 ? 1 : std === 0 ? 0 : null;

  const daily = inf.daily || {};
  const weekly = inf.weekly || {};
  const fourH = inf.h4 || {};
  const monthly = inf.monthly || {};
  // The forensic timeline uses snake_case field names because it was
  // generated by scripts/forensic-timeline.js (Phase 1.2). Map back to
  // the day-state KV shape that worker/trend-hold.js expects.

  return {
    ticker,
    priceClose: inf.close,
    price: inf.close,
    tf_tech: {
      D: {
        ema: {
          priceAboveEma21: daily.ema21_status === 'above' ? true
            : daily.ema21_status === 'below' ? false : null,
        },
        ema5: daily.ema5,
        ema12: daily.ema12,
        rsi: { r5: daily.rsi },
        stDir: toPine(daily.st_dir),
      },
      W: {
        ema: {
          priceAboveEma21: weekly.ema21_status === 'above' ? true
            : weekly.ema21_status === 'below' ? false : null,
        },
        rsi: { r5: weekly.rsi },
        stDir: toPine(weekly.st_dir),
      },
      "4H": {
        ema: {
          priceAboveEma21: fourH.ema21_status === 'above' ? true
            : fourH.ema21_status === 'below' ? false : null,
        },
        rsi: { r5: fourH.rsi },
      },
    },
    monthly_bundle: monthly.st_dir != null ? {
      supertrend_dir: toPine(monthly.st_dir),
    } : null,
    td_sequential: {
      per_tf: {
        W: {
          // weekly TD9 sell-setup count is only set when direction is 'sell'
          bearish_prep_count: weekly.td9_dir === 'sell' ? (weekly.td9_count || 0) : 0,
        },
      },
    },
    sector_rating: null,
    days_to_earnings: 30,
  };
}

function buildTradeForGate(ticker, inf, prevHighMfe) {
  const gainPct = Number(inf.gain_pct) || 0;
  // mfe is at least the gain at this inflection, plus running max.
  const mfe = Math.max(gainPct, prevHighMfe || 0);
  return {
    trade_id: `smoke-${ticker}-${inf.inflection}`,
    ticker,
    direction: 'LONG',
    entryPrice: 100,
    avgEntry: 100,
    maxFavorableExcursion: mfe,
    trimmed_pct: 0,
    trend_hold_state: null,
    trend_hold_demoted_at: null,
  };
}

// ─────────────────────────────────────────────────────────────────────
function loadTimeline(ticker) {
  const p = path.join(TIMELINES_DIR, `${ticker}.json`);
  if (!fs.existsSync(p)) return null;
  return JSON.parse(fs.readFileSync(p, 'utf8'));
}

function listTimelines() {
  return fs.readdirSync(TIMELINES_DIR).filter(f => f.endsWith('.json'))
    .map(f => f.replace('.json', ''));
}

function evaluateTicker(ticker) {
  const t = loadTimeline(ticker);
  if (!t) return { ticker, error: 'no_timeline' };

  const inflections = t.inflections || [];
  let runningMfe = 0;
  const results = [];
  for (const inf of inflections) {
    runningMfe = Math.max(runningMfe, Number(inf.gain_pct) || 0);
    const td = inflectionToTickerData(ticker, inf);
    const tradeForGate = buildTradeForGate(ticker, inf, runningMfe);
    const snap = extractTrendSnapshot(td, tradeForGate);
    if (!snap) {
      results.push({ inflection: inf.inflection, snap: null, promote: false, reason: 'snap_null' });
      continue;
    }
    const r = shouldPromoteToTrendHold(snap, tradeForGate, DEFAULT_TREND_HOLD_CONFIG);
    results.push({
      inflection: inf.inflection,
      gain_pct: inf.gain_pct,
      mfe: tradeForGate.maxFavorableExcursion,
      wkEma21_above: snap.weekly?.ema21_above,
      dEma21_above: snap.daily?.ema21_above,
      hourEma21_above: snap.fourH?.ema21_above,
      monthlyStDir: snap.monthly?.stDir,
      weeklyStDir: snap.weekly?.stDir,
      promote: r.promote,
      reason: r.reason,
      flavor: r.flavor,
    });
  }
  return {
    ticker,
    flavor: t.flavor,
    return_pct: t.return_pct,
    inflections: results,
    promoted_at: results.find(r => r.promote)?.inflection || null,
  };
}

function run() {
  console.log('=== Phase 3 Smoke — Trend-Hold gates against forensic timelines ===\n');

  // Cohort metadata.
  const cohort = JSON.parse(fs.readFileSync(COHORT_JSON, 'utf8'));
  const thByTicker = new Map();
  for (const r of cohort.rows) if (r.trend_hold_candidate) thByTicker.set(r.ticker, r);

  const targets = ONLY_TICKER ? [ONLY_TICKER] : listTimelines();
  const results = targets.map(evaluateTicker).filter(Boolean);

  // ── Section 1: SNDK + blueprint targets ──
  const BLUEPRINT = ['SNDK', 'BE', 'MU', 'SOXL', 'LITE', 'GOOGL', 'WDC'];
  console.log('## Blueprint case-study tickers (must promote)');
  console.log('');
  console.log('| ticker | flavor | return % | promoted at | first promote reason |');
  console.log('|---|---|---:|---|---|');
  let blueprintPromotions = 0;
  for (const tk of BLUEPRINT) {
    const r = results.find(x => x.ticker === tk);
    if (!r) { console.log(`| ${tk} | (no timeline) | — | — | — |`); continue; }
    const firstPromote = r.inflections.find(x => x.promote);
    if (firstPromote) blueprintPromotions++;
    console.log(`| ${tk} | ${r.flavor} | ${r.return_pct?.toFixed(1)} | ${firstPromote?.inflection || 'NEVER'} | ${firstPromote ? `flavor=${firstPromote.flavor} mfe=${firstPromote.mfe?.toFixed(1)}%` : (r.inflections[r.inflections.length-1]?.reason || '?')} |`);
  }
  console.log('');

  // ── Section 2: Counter-examples (must NOT promote) ──
  console.log('## Counter-examples (must NOT promote)');
  console.log('');
  console.log('| ticker | promoted? | first reject reason |');
  console.log('|---|---|---|');
  let counterFalsePositives = 0;
  for (const tk of COUNTER_EXAMPLES) {
    const r = evaluateTicker(tk);
    if (!r || r.error) { console.log(`| ${tk} | (no timeline) | — |`); continue; }
    const firstPromote = r.inflections.find(x => x.promote);
    if (firstPromote) counterFalsePositives++;
    console.log(`| ${tk} | ${firstPromote ? `**YES (FAIL)** at ${firstPromote.inflection}` : 'no'} | ${r.inflections[0]?.reason || '?'} |`);
  }
  console.log('');

  // ── Section 3: Cohort-wide stats ──
  const cohortResults = results.filter(r => thByTicker.has(r.ticker));
  const promotedCount = cohortResults.filter(r => r.inflections.some(x => x.promote)).length;
  console.log('## Cohort-wide promotion rate');
  console.log('');
  console.log(`Total TH-candidate timelines: ${cohortResults.length}`);
  console.log(`Tickers with at least one promotable inflection: **${promotedCount}** / ${cohortResults.length} (${(promotedCount / cohortResults.length * 100).toFixed(0)}%)`);
  const cleanCohort = cohortResults.filter(r => r.flavor === 'CLEAN_TREND');
  const resilCohort = cohortResults.filter(r => r.flavor === 'RESILIENT_TREND');
  const cleanProm = cleanCohort.filter(r => r.inflections.some(x => x.promote)).length;
  const resilProm = resilCohort.filter(r => r.inflections.some(x => x.promote)).length;
  console.log(`  - CLEAN_TREND:     ${cleanProm} / ${cleanCohort.length} promote`);
  console.log(`  - RESILIENT_TREND: ${resilProm} / ${resilCohort.length} promote`);
  console.log('');

  // ── Verbose detail per ticker if requested ──
  if (VERBOSE && ONLY_TICKER) {
    const r = results.find(x => x.ticker === ONLY_TICKER);
    if (r) {
      console.log(`\n## ${ONLY_TICKER} per-inflection detail\n`);
      console.log('| inflection | gain% | mfe | wk↑ | d↑ | 4h↑ | wkST | mST | promote | reason / flavor |');
      console.log('|---|---:|---:|:---:|:---:|:---:|:---:|:---:|:---:|---|');
      for (const x of r.inflections) {
        const mark = (b) => b === true ? '✓' : b === false ? '✗' : '·';
        const dir = (d) => d === 1 ? 'b' : d === -1 ? 's' : d === 0 ? '-' : '·';
        console.log(`| ${x.inflection} | ${x.gain_pct ?? '-'} | ${x.mfe?.toFixed(1)} | ${mark(x.wkEma21_above)} | ${mark(x.dEma21_above)} | ${mark(x.hourEma21_above)} | ${dir(x.weeklyStDir)} | ${dir(x.monthlyStDir)} | ${x.promote ? '**YES**' : 'no'} | ${x.flavor || x.reason} |`);
      }
    }
  }

  // ── Pass criteria ──
  console.log('## Pass criteria');
  console.log('');
  let allPass = true;
  if (blueprintPromotions >= 5) {
    console.log(`PASS  Blueprint targets: ${blueprintPromotions} / ${BLUEPRINT.length} promoted (≥ 5 required).`);
  } else {
    console.log(`FAIL  Blueprint targets: ${blueprintPromotions} / ${BLUEPRINT.length} promoted (< 5 required).`);
    allPass = false;
  }
  if (counterFalsePositives === 0) {
    console.log(`PASS  Counter-examples: 0 false-positive promotions.`);
  } else {
    console.log(`FAIL  Counter-examples: ${counterFalsePositives} false-positive promotions.`);
    allPass = false;
  }
  const cohortRate = promotedCount / cohortResults.length;
  if (cohortRate >= 0.8) {
    console.log(`PASS  Cohort-wide rate: ${(cohortRate * 100).toFixed(0)}% (≥ 80% required).`);
  } else {
    console.log(`FAIL  Cohort-wide rate: ${(cohortRate * 100).toFixed(0)}% (< 80% required — gates may be too strict for the actual data shape).`);
    allPass = false;
  }
  console.log('');
  console.log(allPass ? 'OVERALL: PASS — gates fire correctly against real forensic data.' : 'OVERALL: FAIL — investigate before re-flipping live flag.');
  process.exit(allPass ? 0 : 1);
}

run();
